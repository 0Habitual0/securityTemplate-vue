<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" auto-complete="on" label-position="left">

      <div class="title-container">
        <h3 class="title">欢迎使用springSecurity模板项目</h3>
      </div>

      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="username"
          v-model="loginForm.username"
          placeholder="请输入用户名"
          name="username"
          type="text"
          tabindex="1"
          auto-complete="on"
        />
      </el-form-item>

      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input
          :key="passwordType"
          ref="password"
          v-model="loginForm.password"
          :type="passwordType"
          placeholder="请输入密码"
          name="password"
          tabindex="2"
          auto-complete="on"
          @keyup.enter.native="handleLogin"
        />
        <span class="show-pwd" @click="showPwd">
          <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
        </span>
      </el-form-item>

      <el-radio v-model="loginForm.role" label="普通用户">普通用户</el-radio>
      <el-radio v-model="loginForm.role" label="管理员" style="margin-bottom: 20px">管理员</el-radio>

      <el-button :loading="loading" type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="handleLogin">登录</el-button>

      <div class="options">
        <el-button v-if="loginForm.role === '普通用户'" type="text" @click="showRegisterDialogFunction()">注册账号</el-button>
        <el-button v-if="loginForm.role === '普通用户'" type="text" @click="showRecoverPasswordDialogFunction()">找回密码</el-button>
      </div>

    </el-form>
    <div>
      <el-dialog
        title="注册账号"
        :visible.sync="showRegisterDialog"
        destroy-on-close
      >
        <Register @close="closeRegisterDialogFunction()" />
      </el-dialog>
      <el-dialog
        title="找回密码"
        :visible.sync="showRecoverPasswordDialog"
        destroy-on-close
      >
        <RecoverPassword @close="closeRecoverPasswordDialogFunction()" />
      </el-dialog>
    </div>
  </div>
</template>

<script>
import Register from '@/views/login/components/register.vue'
import RecoverPassword from '@/views/login/components/recoverPassword.vue'

export default {
  name: 'Login',
  components: { Register, RecoverPassword },
  data() {
    const validateUsername = (rule, value, callback) => {
      if (!value) {
        callback(new Error('请输入用户名'))
      } else {
        callback()
      }
    }
    const validatePassword = (rule, value, callback) => {
      if (!value) {
        callback(new Error('请输入密码'))
      } else {
        callback()
      }
    }
    return {
      loginForm: {
        role: '普通用户'
      },
      loginRules: {
        username: [{ required: true, trigger: 'blur', validator: validateUsername }],
        password: [{ required: true, trigger: 'blur', validator: validatePassword }]
      },
      loading: false,
      passwordType: 'password',
      redirect: undefined,
      showRegisterDialog: false,
      showRecoverPasswordDialog: false
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  methods: {
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true
          this.$store.dispatch('user/login', this.loginForm).then(() => {
            this.$router.push({ path: this.redirect || '/' })
            this.loading = false
          }).catch(() => {
            this.loading = false
          })
        } else {
          return false
        }
      })
    },
    showRegisterDialogFunction() {
      this.showRegisterDialog = true
    },
    showRecoverPasswordDialogFunction() {
      this.showRecoverPasswordDialog = true
    },
    closeRegisterDialogFunction() {
      this.showRegisterDialog = false
    },
    closeRecoverPasswordDialogFunction() {
      this.showRecoverPasswordDialog = false
    }
  }
}
</script>

